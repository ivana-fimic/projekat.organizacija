    private void txtNazivZadatkaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNazivZadatkaKeyReleased
        String param = txtNazivZadatka.getText();

        if (param.matches("\\d+")) {

            JOptionPane.showMessageDialog(this, " Nije dobar format unosa,naziv nema brojeve");
            return;
        }
        ModelTabeleZadatak mt = (ModelTabeleZadatak) tblZadaci.getModel();

        mt.setParametar(param);


    }//GEN-LAST:event_txtNazivZadatkaKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = tblZadaci.getSelectedRow();

        if (row != -1) {

            Zadatak z = ((ModelTabeleZadatak) tblZadaci.getModel()).vratiOdabraniZadatak(row);

             try {
              Zadatak ucitanIzBaze=KlijentController.getInstance().ucitajZadatak(z);
               SwingUtilities.invokeLater(new Runnable() {
                  public void run() {
                    try {
                       new DetaljiZadatka(PretraziZadatak.this,null, true, ucitanIzBaze,Moodovi.PRIKAZ).setVisible(true);
                    } catch (Exception ex) {
                        java.util.logging.Logger.getLogger(PretraziClana.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
    }
               }); 
                SwingUtilities.invokeLater(new Runnable() {
        public void run() {
            JOptionPane.showMessageDialog(null, "Sistem je ucitao zadatak", "POTVRDA", JOptionPane.INFORMATION_MESSAGE);
        }
    });
               
               
           } catch (Exception ex) {
               java.util.logging.Logger.getLogger(PretragaTima.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
           }
        }
        else{
             JOptionPane.showMessageDialog(null, "Sistem ne moze da ucita zadatak", "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

